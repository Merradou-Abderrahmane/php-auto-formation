<?php
// indexed arrays
$fruits = array('banana', 'apple' , 'manga' );
echo 'fruits : ' .$fruits[0] ."  "  .$fruits[1] ;
// associative arrays
$fruits = array('banana' => 'yellow',
                'apple' => 'red' ,
                 'manga' => 'green'
              );
echo 'fruits : ' .$fruits['banana'] ."  "  .$fruits['apple'];   
?>
